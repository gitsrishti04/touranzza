              <h3 class="tour-listing-details__title tour-listing-details__plan-title">Day Wise Itinerary</h3>
              <div class="trevlo-accrodion tour-listing-details__faq" data-grp-name="tour-listing-details__faq">
                
                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 1</span> Arrival in Delhi</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Your driver meets you at Indira Gandhi International Airport and transfers you to your hotel in a quiet neighbourhood of the capital. After the long flight, the day is kept light — a chance to settle in, rest, and adjust. If energy permits, a short evening walk through Khan Market or Lodhi Gardens is a gentle way to begin your India journey. Overnight in Delhi.</p>
                    </div>
                  </div>
                </div>
                
                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 2</span> Exploring Old &amp; New Delhi</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">A full day in the two Delhis. The morning belongs to the old city — the soaring courtyards of Jama Masjid, the red sandstone ramparts of the Red Fort, and the quiet memorial at Raj Ghat where Mahatma Gandhi was cremated. A cycle-rickshaw ride through Chandni Chowk's crowded lanes is optional and memorable.</p>
                      <p style="margin-top: 0.75rem;">After lunch, you shift centuries to colonial and imperial Delhi — Humayun's Tomb, considered the architectural blueprint for the Taj Mahal, and the tapered minaret of Qutub Minar. A slow drive along Rajpath past India Gate and the Presidential Estate closes the day. Overnight in Delhi.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 3</span> Delhi to Agra &amp; the Taj Mahal</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">A morning drive on the Yamuna Expressway brings you to Agra in roughly three and a half hours. After checking into your hotel, you visit the Taj Mahal in the soft afternoon light, when the marble takes on warmer tones and the crowds begin to thin. Your guide will share the Mughal love story behind it and the craft that consumed two decades and twenty thousand artisans.</p>
                      <p style="margin-top: 0.75rem;">Later, Agra Fort — the garrisoned red sandstone complex from which Shah Jahan is said to have watched the Taj through his final years of captivity. Overnight in Agra.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 4</span> Agra to Jaipur via Fatehpur Sikri</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Today is a journey day, but a rewarding one. You leave Agra for Jaipur with a long stop at Fatehpur Sikri, Akbar's short-lived red sandstone capital. Built to honour a Sufi saint's prophecy and abandoned within fifteen years, it is remarkably well preserved — the Buland Darwaza, Panch Mahal and Diwan-i-Khas are highlights.</p>
                      <p style="margin-top: 0.75rem;">You continue to Jaipur and arrive by late afternoon, with enough time to rest before dinner. Overnight in Jaipur.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 5</span> The Pink City of Jaipur</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Morning begins with a short drive out of the city to Amber Fort, the former capital of the Kachwaha rulers, perched above a narrow lake. You can ascend by jeep or, if you prefer, by elephant — your guide will walk you through the Sheesh Mahal, Diwan-i-Aam and the carved marble courtyards above.</p>
                      <p style="margin-top: 0.75rem;">The afternoon covers the old walled city — the honeycombed façade of Hawa Mahal, the astronomical instruments of Jantar Mantar, and the City Palace Museum with its Mughal miniatures and royal textiles. Evenings in Jaipur are best spent in the bazaars of Johari and Bapu for block-prints, blue pottery and silver. Overnight in Jaipur.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 6</span> Jaipur to the Blue City of Jodhpur</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">A longer drive today, roughly six hours across the Aravalli range to Jodhpur, where a wash of indigo-painted houses fans out beneath the cliff-edge Mehrangarh Fort. After check-in and lunch, you climb to the fort — widely considered the finest in Rajasthan — with sweeping views over the old city below. A visit to Jaswant Thada, the milk-white cenotaph of Maharaja Jaswant Singh, completes the afternoon. Evening is free for the Sardar Bazaar clock-tower area. Overnight in Jodhpur.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 7</span> Jodhpur to Udaipur via Ranakpur</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Leaving Jodhpur after breakfast, you drive through open countryside to Ranakpur, where a fifteenth-century Jain temple complex rises in pale marble from a forested valley. Its 1,444 carved pillars — each unique — are one of Rajasthan's quietest marvels and often missed by rushed itineraries.</p>
                      <p style="margin-top: 0.75rem;">After a temple lunch, the drive continues into the Mewar region and onwards to Udaipur, where you arrive in time for a slow evening by the lake. Overnight in Udaipur.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 8</span> Udaipur, the City of Lakes</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">A lighter day to match the city's mood. Morning begins at the City Palace — a vast complex of courtyards, balconies and mirror-inlaid chambers built over four centuries. You'll visit Sahelion Ki Bari, the royal ladies' garden with its lotus ponds and marble elephants, the folk art collection at Bhartiya Lok Kala Mandal, and Jagdish Temple at the heart of the old town.</p>
                      <p style="margin-top: 0.75rem;">In the late afternoon, a boat ride on Lake Pichola — the signature Udaipur experience — brings you past Jag Mandir and the shimmering white façade of Taj Lake Palace as the sun drops behind the Aravallis. Overnight in Udaipur.</p>
                    </div>
                  </div>
                </div>

