                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;"><strong>Morning:</strong> Visit Amber Fort — the grand hilltop Rajput fortress built by Raja Man Singh I in 1592. Ascend by jeep along the old elephant road. Inside: the Ganesh Pol ceremonial gateway, the Sheesh Mahal (Hall of Mirrors) where a single candle flame multiplies across 1,000 mirrored facets, and the zenana courtyards. Photo stop at Jal Mahal — the water palace floating on Man Sagar Lake.</p>
                      <p style="margin-top: 0.75rem;"><strong>Afternoon:</strong> City Palace — the royal residential complex at the city's heart, with its collection of royal costumes, weapons and the famous pair of silver urns (the world's largest silver objects). Jantar Mantar — the UNESCO astronomical observatory with 19 precision instruments, including the Samrat Yantra, the world's largest stone sundial. Hawa Mahal — the 953-windowed pink sandstone screen from which Jaipur's royal ladies observed street life unseen.</p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Jaipur</p>
                    </div>
