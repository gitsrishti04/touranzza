                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;"><strong>Morning:</strong> Sunrise entry to the Taj Mahal. Entering through the Darwaza-i-Rauza — Shah Jahan's great gateway — what lies beyond has moved travellers for nearly four centuries. Your guide walks you through the 22-year construction by 20,000 artisans, the extraordinary pietra dura gemstone inlay work across the marble surfaces, and the optical corrections built into the four minarets. Time to walk the gardens and approach the plinth at your own pace. <strong>Note: The Taj Mahal is closed on Fridays.</strong></p>
                      <p style="margin-top: 0.75rem;"><strong>Afternoon:</strong> Agra Fort — the UNESCO World Heritage red sandstone citadel that served as the Mughal imperial residence from Akbar to Aurangzeb. Inside: the Jahangiri Mahal, the white marble Khas Mahal and the Musamman Burj — the octagonal tower from which Shah Jahan, in his final captive years, is said to have gazed toward the Taj. Optional sunset visit to Mehtab Bagh — the Mughal cross-garden directly opposite the Taj, for a quiet riverfront view without crowds.</p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Agra</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 6</span> Agra to Orchha &mdash; Train to Jhansi, Drive to Orchha</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">After breakfast, transfer to Agra Cantonment Station for the train to Jhansi — a comfortable 2-hour journey through the Chambal plains into Madhya Pradesh. Private car meets you at Jhansi Station for the 18-km drive to Orchha. Hotel check-in — ideally a heritage property within the old town itself.</p>
                      <p style="margin-top: 0.75rem;">After lunch, afternoon guided walk through Orchha: Raja Mahal — the principal Bundela palace begun by Madhukar Shah in the 1550s, with extraordinary gem-tone murals depicting Vishnu's ten avatars. Jahangir Mahal — built to commemorate Emperor Jahangir's 1606 visit, featuring a rooftop terrace overlooking the Betwa River. Laxminarayan Temple — a fortified temple whose interiors carry 17th-century murals among the finest surviving examples of Bundelkhand painting. End the afternoon walking the Betwa Riverbank, where 14 royal cenotaphs — chhatris — stand in various states of picturesque decay.</p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Orchha</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 7</span> Orchha to Khajuraho &mdash; 180 km Drive</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Morning at leisure in Orchha — one of the rare places on this itinerary that rewards simply wandering without a schedule. The town wakes slowly; the temples are used for active worship, and the Betwa at dawn is particularly quiet. Optional early morning visit to Ram Raja Mandir, where the presiding deity Ram has been worshipped as a king since the 17th century under a protocol adopted from Bundela royal court ceremonies — the only temple in India where Ram is accorded a full royal guard of honour.</p>
                      <p style="margin-top: 0.75rem;">After a late morning departure, drive approximately 180 km (3 hours) east to Khajuraho. Hotel check-in. Late afternoon optional visit to the Sound and Light Show at the Western Group — a 50-minute narrated history of the Chandela dynasty that built these temples, projected against the stone façades after dark. A good introduction before the main temple visit tomorrow morning.</p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Khajuraho</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 8</span> Khajuraho &mdash; Western &amp; Eastern Temple Groups</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;"><strong>Morning — Western Group (UNESCO, minimum 3 hours):</strong> The centrepiece of Khajuraho. Built between 950 and 1050 AD by the Chandela Rajput dynasty, the 20 surviving temples are among the finest examples of Nagara-style temple architecture in India. Begin with Kandariya Mahadeva — the largest and most elaborately carved, rising 31 metres with over 900 sculptures across its exterior. Your guide walks you through the iconographic programme: the lower registers showing celestial maidens (apsaras) and attendant figures; the middle bands with the famous mithunas — sensuous and erotic couples interpreted within a Tantric tradition that positioned human desire as a gateway to divine consciousness; and the upper registers approaching the shikhara tower, showing increasingly abstract divine forms. The erotic panels represent approximately 10% of the total sculptural surface — the remaining 90% depicts gods, mythological narratives, warriors and daily life, all executed with a sophistication that places Khajuraho among the great sculptural achievements of the medieval world.</p>
                      <p style="margin-top: 0.75rem;">Also visit: Lakshmana Temple (dedicated to Vishnu, with a particularly complete sculptural programme); Matangesvara Temple (still used for active worship, notably different in atmosphere from the archaeological group); and Varaha Temple, housing a monolithic boar sculpture.</p>
                      <p style="margin-top: 0.75rem;"><strong>Afternoon — Eastern Group (for those with deeper interest, 1–2 hours):</strong> A smaller cluster including Jain temples — Parsvanatha Temple in particular has carved panels of comparable quality to the Western Group. The Eastern Group is often quieter and rewards slower inspection.</p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Khajuraho</p>
                    </div>
                  </div>
                </div>

