            <div class="tour-listing-details__plan">
              <h3 class="tour-listing-details__title tour-listing-details__plan-title">Day Wise Itinerary</h3>
              <div class="trevlo-accrodion tour-listing-details__faq" data-grp-name="tour-listing-details__faq">
                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 1</span> Arrival in Delhi</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Your driver meets you at Indira Gandhi International Airport and transfers you to your hotel. The day is kept deliberately light — time to shower, rest, and let the time-zone shift settle before the itinerary begins in earnest. If you arrive early and feel inclined, a slow walk through Lodhi Gardens at dusk is a gentle first impression of India. Overnight in Delhi.</p>
                    </div>
                  </div>
                </div>
                
                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 2</span> A Full Day in Old &amp; New Delhi</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">After breakfast, the morning is spent in Old Delhi — the vast red sandstone courtyards of Jama Masjid, a cycle-rickshaw ride through the spice and jewellery lanes of Chandni Chowk, and a quiet stop at Raj Ghat, where Mahatma Gandhi was cremated. The Red Fort's exterior frames your route, and you may choose to enter if your energy holds.</p>
                      <p style="margin-top: 0.75rem;">In the afternoon, you shift to imperial and colonial Delhi. Humayun's Tomb — the Mughal prototype that would later inform the Taj Mahal — sets the architectural context for the Rajasthan route ahead. Qutub Minar, the soaring 12th-century victory tower, and a drive past India Gate and the Presidential Estate round out the day. Overnight in Delhi.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 3</span> Delhi to Jaipur</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">A morning drive of roughly five and a half hours on the Delhi–Jaipur expressway brings you to the Pink City. After check-in and lunch, the afternoon is yours — ideal for the Albert Hall Museum, Rambagh's gardens if you're curious about Jaipur's palace hotels, or simply a walk through the nearby bazaars to orient yourself. Overnight in Jaipur.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 4</span> Jaipur: The Pink City and Amber Fort</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Morning begins with the short drive to Amber Fort, perched above a narrow lake at the edge of the Aravallis. You can ascend by jeep or by elephant — your guide walks you through the Ganesh Pol, the mirror-inlaid Sheesh Mahal and the carved marble apartments above. Jaigarh and Nahargarh crown the ridges above and can be added if time permits.</p>
                      <p style="margin-top: 0.75rem;">Afternoon is given to the walled city. The honeycombed façade of Hawa Mahal, the astronomical observatory of Jantar Mantar, and the City Palace Museum with its Mughal miniatures and royal textiles fill a leisurely three hours. A photo stop at Jal Mahal, set in the middle of Man Sagar Lake, closes the day. Evening is free for dinner at a local heritage restaurant. Overnight in Jaipur.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 5</span> Jaipur to Ajmer &amp; Pushkar</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">You leave Jaipur after breakfast and drive roughly two and a half hours to Ajmer, where the Dargah of Khwaja Moinuddin Chishti — the most important Sufi shrine in the subcontinent — draws pilgrims from across the Muslim world. Your guide accompanies you through the outer courts; the atmosphere is devotional and your time here should be quiet. You'll also see the Adhai Din Ka Jhonpra mosque, built from the remains of earlier Hindu and Jain temples, and Ana Sagar Lake.</p>
                      <p style="margin-top: 0.75rem;">A short drive of about half an hour takes you onwards to Pushkar, one of India's oldest pilgrimage towns. The evening aarti at the lakeside ghats is unforgettable. Overnight in Pushkar.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 6</span> Pushkar to Udaipur</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Before leaving Pushkar, you visit the Brahma Temple — one of very few temples to the creator god anywhere in India — and walk the ghats that ring the sacred lake. After breakfast the drive to Udaipur begins, roughly five to six hours through the Aravalli range and open countryside. You arrive in the late afternoon with time for a slow evening by the lake. Overnight in Udaipur.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 7</span> Udaipur: The City of Lakes</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">The day is given to Udaipur's royal architecture. Morning begins at the City Palace, a vast complex of courtyards, balconies and mirror-inlaid chambers built over four centuries by the Mewar rulers. Jagdish Temple, Bagore Ki Haveli with its miniature painting collection, and Saheliyon Ki Bari — the royal ladies' garden — fill the rest of the morning.</p>
                      <p style="margin-top: 0.75rem;">The late afternoon is reserved for a boat ride on Lake Pichola, past Jag Mandir and the shimmering Taj Lake Palace, timed so you are on the water as the sun drops behind the Aravallis. Shilpgram, the rural arts complex, is optional for travellers interested in Rajasthani craft traditions. Overnight in Udaipur.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 8</span> Udaipur to Mount Abu</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">A relaxed drive of roughly four hours brings you from the lakes of Udaipur up into the Aravalli foothills and Rajasthan's only hill station. Mount Abu is cooler and greener than the plains — a welcome pause at this point in the itinerary. After check-in, a late-afternoon walk around Nakki Lake and a visit to Sunset Point are the classic first impressions. Overnight in Mount Abu.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 9</span> Mount Abu to Jodhpur via Dilwara</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
