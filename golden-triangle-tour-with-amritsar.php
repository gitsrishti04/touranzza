                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 2</span> Old &amp; New Delhi &mdash; Mughal Legacy to Imperial Grandeur</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;"><strong>Morning — Old Delhi:</strong> Begin at Jama Masjid — Shah Jahan's great congregational mosque, completed in 1656, with a courtyard accommodating 25,000 worshippers. Explore Chandni Chowk by cycle-rickshaw, weaving through spice lanes, silver markets and colonial-era sweet shops that have traded here for over 350 years. Drive past the imposing sandstone facade of Red Fort (exterior), then stop at Raj Ghat — the black marble platform marking Mahatma Gandhi's cremation site on the Yamuna's west bank.</p>
                      <p style="margin-top: 0.75rem;"><strong>Afternoon — New Delhi:</strong> Visit Humayun's Tomb — the 1570 UNESCO World Heritage mausoleum that architecturally prefigured the Taj Mahal with its Charbagh garden, white marble dome and red sandstone composition. Continue to Qutub Minar — the 73-metre UNESCO-listed victory tower begun in 1193, flanked by the 2,000-year-old Iron Pillar. End with a panoramic drive past India Gate, Parliament House, and Rashtrapati Bhavan — Lutyens' grand imperial axis, now the ceremonial core of the world's largest democracy.</p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Delhi</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 3</span> Delhi to Agra &mdash; The Taj Mahal &amp; Agra Fort</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">After breakfast, depart for Agra via the Yamuna Expressway — approximately 200 km, around 3 to 3.5 hours. Hotel check-in and a brief rest before your private guide takes you to the Taj Mahal. Entering through the Darwaza-i-Rauza, the great Mughal gateway, what lies beyond has moved travellers for nearly four centuries. Your guide walks you through Shah Jahan's grief after Mumtaz Mahal's death in 1631, the 22-year construction by 20,000 artisans, the extraordinary pietra dura gemstone inlay, and the optical corrections built into the minarets.</p>
                      <p style="margin-top: 0.75rem;">Continue to Agra Fort — the UNESCO World Heritage red sandstone citadel that served as the Mughal imperial residence from Akbar to Aurangzeb. Inside: the Jahangiri Mahal, the white marble Khas Mahal, and the Musamman Burj, from which Shah Jahan is said to have gazed toward the Taj in his final captive years. A sunset visit to Mehtab Bagh — the Mughal garden directly opposite the monument — offers a quiet, crowd-free riverfront view. <strong>Note: The Taj Mahal is closed on Fridays.</strong></p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Agra</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
