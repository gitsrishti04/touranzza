                      <p style="margin-top: 0.75rem;">The evening is ideal for a first visit to the Golden Temple, Harmandir Sahib — illuminated after dark, its gold-plated façade reflecting across the sacred Sarovar. The atmosphere at night — quieter, more contemplative, the sounds of kirtan (devotional music) drifting across the water — offers one of India's most serene and memorable first impressions of a spiritual site.</p>
