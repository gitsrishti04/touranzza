                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Optional early Taj Mahal sunrise visit before breakfast — predawn entry when the marble shifts from pearl grey to warm gold, with the complex almost entirely to yourselves. Strongly recommended for those who want an entirely different encounter with the monument from the previous evening.</p>
                      <p style="margin-top: 0.75rem;">After breakfast and hotel check-out, depart for Jaipur with a guided stop at Fatehpur Sikri — Emperor Akbar's extraordinary red sandstone capital, built between 1569 and 1585 and abandoned within 14 years. Explore Buland Darwaza, the 54-metre ceremonial gateway — the world's largest; Diwan-i-Khas with its unique central column; the five-storey Panch Mahal; and the Salim Chishti Dargah within the Jami Masjid courtyard. Continue to Jaipur. Hotel check-in, then the evening is free for the bazaars — Johari Bazaar for gemstones, Bapu Bazaar for block-print fabrics, or a local cultural dinner.</p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Jaipur</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 5</span> Jaipur &mdash; Royal Rajasthan &amp; Amber Fort</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;"><strong>Morning:</strong> Visit Amber Fort — the grand Rajput hilltop fortress built by Raja Man Singh I in 1592, ascended by jeep along the old elephant road. Explore the Ganesh Pol gateway, the Sheesh Mahal (Hall of Mirrors), the zenana courtyards, and the Maota Lake panorama below. Photo stop at Jal Mahal — the water palace reflected in Man Sagar Lake with the Aravalli Hills behind it.</p>
                      <p style="margin-top: 0.75rem;"><strong>Afternoon:</strong> City Palace — the royal complex at the heart of the walled city, with its museums of royal costumes, miniature paintings, weapons, and the iconic pair of silver urns (the world's largest silver objects). Next: Jantar Mantar — the UNESCO-listed astronomical observatory with 19 instruments, including the Samrat Yantra, the world's largest stone sundial. End at Hawa Mahal — the 953-windowed pink sandstone screen facade, from which Jaipur's royal ladies once observed street life unseen.</p>
                      <p style="margin-top: 0.75rem;"><strong>Evening:</strong> Optional shopping in Johari Bazaar for Rajasthani gemstones, textiles and lacquerware.</p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Jaipur</p>
                    </div>
