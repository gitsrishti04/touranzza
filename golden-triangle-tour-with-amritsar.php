                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">This is the emotional and cultural centrepiece of the tour — a day that moves effortlessly between deep spiritual peace and the weight of historical memory.</p>
                      <p style="margin-top: 0.75rem;"><strong>Morning — Golden Temple:</strong> Return to Harmandir Sahib in the morning light. Cover your head, remove your shoes and wade through the cleansing pool at the entrance before crossing the causeway (Guru's Bridge) to the sanctum. The holiest shrine in Sikhism, it was built in its current form under Maharaja Ranjit Singh in the early 19th century, with the gold-plated copper of its upper floors donated by Ranjit Singh himself. Witness the Sarovar — the sacred pool surrounding the temple — at its most luminous in morning sun. Experience the Langar — the community kitchen that has served free meals to all visitors, regardless of faith or background, every day for over 400 years — a living expression of Sikh values of service and equality.</p>
                      <p style="margin-top: 0.75rem;"><strong>Midday — Jallianwala Bagh:</strong> Walk just minutes from the Golden Temple to Jallianwala Bagh — the walled garden where on 13 April 1919, British troops under General Dyer opened fire on an unarmed crowd gathered for Baisakhi celebrations. Between 379 and 1,000 people were killed (estimates vary). The memorial stands on the site, including the bullet-scarred walls and the well into which many jumped to escape. This is not a comfortable place; it is an important one — a critical chapter in the story of India's independence and the moral reckoning of empire.</p>
                      <p style="margin-top: 0.75rem;">Optional: The Partition Museum on Town Hall Road offers a deeply researched, profoundly moving account of the 1947 Partition of India and Pakistan, with personal testimonies, archival photographs and artefacts. One of the finest historical museums in South Asia.</p>
                      <p style="margin-top: 0.75rem;"><strong>Evening — Wagah Border Beating Retreat Ceremony:</strong> Drive approximately 30 km to the Wagah Border crossing between India and Pakistan. Every evening at sunset, the Border Security Force of India and Pakistan Rangers perform the Beating Retreat — an elaborately choreographed flag-lowering ceremony combining military precision, nationalistic fervour, and a moment of shared ritual between two countries that have been at war four times. The atmosphere in the grandstand — crowds singing, flags waving, enormous soldiers in ceremonial dress — is unlike any other experience on this itinerary.</p>
