                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Your journey begins in Delhi — India's historic gateway and a city where multiple civilisations have shaped one extraordinary capital. On arrival at Indira Gandhi International Airport, a private representative meets you with a name board and transfers you comfortably to your hotel.</p>
                      <p style="margin-top: 0.75rem;">The first day is intentionally relaxed, allowing time to recover from international travel while adjusting to India's atmosphere. Depending on your arrival time, optional evening experiences include a gentle stroll around India Gate and the illuminated Kartavya Path, Connaught Place's colonial arcades, or the serene Lodhi Garden — a soft and unhurried entry point to the city before the itinerary begins in earnest tomorrow.</p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Delhi</p>
                    </div>
