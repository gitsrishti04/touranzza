                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 2</span> Delhi to Mandawa (Shekhawati)</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Morning is given to Delhi's headline sights. Old Delhi first: the soaring courtyards of Jama Masjid, a cycle-rickshaw ride through the spice lanes of Chandni Chowk, and a quiet stop at Raj Ghat where Mahatma Gandhi was cremated. The Red Fort's exterior frames your route. Then imperial Delhi: Humayun's Tomb and Qutub Minar, with a drive past India Gate and the Presidential Estate.</p>
                      <p style="margin-top: 0.75rem;">After lunch, you drive roughly five hours north-west to Mandawa in the Shekhawati region, where 18th- and 19th-century merchant havelis are covered in painted frescoes. Check in and rest. Evening is free to walk the town. Overnight in Mandawa.</p>
                    </div>
