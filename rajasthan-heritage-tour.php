                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Morning begins with the short drive to Amber Fort, perched above a narrow lake at the edge of the Aravallis. You can ascend by jeep or by elephant — your guide walks you through the Ganesh Pol, the mirror-inlaid Sheesh Mahal and the carved marble apartments above.</p>
                      <p style="margin-top: 0.75rem;">Afternoon is given to the walled city. The honeycombed façade of Hawa Mahal, the astronomical observatory of Jantar Mantar, and the City Palace Museum with its Mughal miniatures and royal textiles fill a leisurely three hours. A photo stop at Jal Mahal, set in the middle of Man Sagar Lake, closes the day. Evening is free for the bazaars of Johari and Bapu. Overnight in Jaipur.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
