                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Morning is given to Udaipur's royal architecture. The City Palace complex — a vast sprawl of courtyards, balconies and mirror-inlaid chambers built over four centuries — fills two hours easily. Jagdish Temple, Bagore Ki Haveli and Saheliyon Ki Bari (the royal ladies' garden) follow. A boat ride on Lake Pichola in the late morning brings you past Jag Mandir and the shimmering Taj Lake Palace.</p>
                      <p style="margin-top: 0.75rem;">After lunch, you drive roughly five hours north-east to Pushkar, one of India's oldest pilgrimage towns. The evening aarti at the lakeside ghats is unforgettable. Overnight in Pushkar.</p>
