                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Morning begins with a guided tour of Junagarh Fort, one of the few major Rajasthani forts not built on a hill. The interiors — mirrored halls, carved balconies, gold-leaf ceilings — are among the finest in the state. If time permits and you're curious, the Karni Mata Temple is roughly 30 km south; otherwise, the National Research Centre on Camel is a more conventional stop.</p>
                      <p style="margin-top: 0.75rem;">After lunch, the drive to Jaisalmer begins — roughly six hours across open scrubland and the edge of the Thar Desert. You arrive in time for sunset over the golden fort. Overnight in Jaisalmer.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 5</span> Jaisalmer: The Golden City &amp; Lodurva</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">A full day in Jaisalmer. Morning is given to the fort itself — a living fort where a quarter of the old city still resides within the walls. You'll visit the Jain temples, the intricately carved Patwon Ki Haveli and Salim Singh Ki Haveli, and walk the narrow lanes where life continues much as it has for centuries. Gadisar Lake and the view from the ramparts close the morning.</p>
                      <p style="margin-top: 0.75rem;">Afternoon is an excursion to Lodurva, 16 km north-west, where a group of ancient Jain temples stand in remarkable preservation. The ornate arches and the divine Kalptaru tree within the main temple are highlights. Return to Jaisalmer by evening. Overnight in Jaisalmer.</p>
                    </div>
                  </div>
                </div>

