                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Morning is spent exploring Mandawa's painted havelis — the Hanuman Prasad Goenka Haveli, Murmuria Haveli and Gulab Rai Ladia Haveli are highlights, each covered in frescoes depicting gods, merchants, trains and early motor cars. The Shekhawati region is often called an "open-air art gallery" and the comparison holds.</p>
                      <p style="margin-top: 0.75rem;">After lunch, you drive west roughly four to five hours to Bikaner, a desert-edge city known for its red sandstone Junagarh Fort and the extraordinary Karni Mata Temple, where thousands of rats are worshipped. You arrive in the late afternoon. Overnight in Bikaner.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
