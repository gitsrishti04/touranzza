                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Your driver meets you at Indira Gandhi International Airport and transfers you to your hotel. After the long flight, the day is kept light — a chance to settle in, rest and adjust to the time difference. If you arrive early and energy permits, a short walk through Lodhi Gardens or Khan Market is a gentle introduction. Overnight in Delhi.</p>
                    </div>
