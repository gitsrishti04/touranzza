                      <p style="margin-top: 0.75rem;">After breakfast, you leave Jaisalmer and drive roughly five to six hours east to Jodhpur, where a wash of indigo-painted houses fans out beneath the cliff-edge Mehrangarh Fort. After check-in and lunch, the afternoon is given to the fort — widely considered the most dramatic in India, with sweeping views over the old city below and one of the finest collections of Rajput armoury, textiles and howdahs in the country.</p>
                      <p style="margin-top: 0.75rem;">Jaswant Thada, the milk-white marble cenotaph of Maharaja Jaswant Singh, is a short stop on the way down. Evening is free for the Sardar Bazaar clock-tower area. Overnight in Jodhpur.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 7</span> Jodhpur to Udaipur via Ranakpur</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Leaving Jodhpur after breakfast, you drive south through open countryside to Ranakpur, where a 15th-century Jain temple complex rises in pale marble from a forested valley. Its 1,444 carved pillars — each unique — are one of Rajasthan's quietest marvels and often missed by rushed itineraries.</p>
                      <p style="margin-top: 0.75rem;">After a temple lunch, the drive continues into the Mewar region and onwards to Udaipur, where you arrive in time for a slow evening by the lake. Overnight in Udaipur.</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
