                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">You leave Jaipur after breakfast and drive roughly four to five hours to Agra, with a long stop at Fatehpur Sikri — Akbar's short-lived red sandstone capital. Built to honour a Sufi saint's prophecy and abandoned within fifteen years, it is remarkably well preserved. The Buland Darwaza, Panch Mahal and Diwan-i-Khas are highlights.</p>
                      <p style="margin-top: 0.75rem;">You continue to Agra and arrive in the mid-afternoon. After check-in and a short rest, you visit the Taj Mahal in the soft late-afternoon light, when the marble takes on warmer tones and the crowds begin to thin. Your guide will share the Mughal love story behind it and the craft that consumed two decades and twenty thousand artisans. Later, Agra Fort — the garrisoned red sandstone complex from which Shah Jahan is said to have watched the Taj through his final years of captivity. Overnight in Agra.</p>
                    </div>
                  </div>
                </div>

